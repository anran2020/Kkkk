//-----------------------------------------------------------------------------
// 函数名：ltimer_create，创建环回定时器
//-----------------------------------------------------------------------------
//
// 返回值 : 
//  LTimerPtr:返回创建对象
// 参数   :  void
//
//功能描述：
//		创建一个环回定时器。
//
//修改履历：
//1.zzx ：  2014-12-01  ：创建函数
//
//-----------------------------------------------------------------------------
LTimerPtr ltimer_create(void)
{
	LTimerPtr ptmr;
	ptmr=mlos_malloc(e_mem_sram,sizeof(LTimer));

	ptmr->initialTime=0;
	ptmr->period=0;

	mlos.ltimerCount++;
	return (ptmr);

}

